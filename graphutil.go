func getBool(s string, def bool) bool {
	if s == "" {
		return def
	}

	switch s {
	case "True", "true", "1":
		return true
	case "False", "false", "0":
		return false
	}

	return def
}

func getString(s string, def string) string {
	if s == "" {
		return def
	}

	return s
}

func getFloat64(s string, def float64) float64 {
	if s == "" {
		return def
	}

	n, err := strconv.ParseFloat(s, 64)
	if err != nil {
		return def
	}

	return n
}

